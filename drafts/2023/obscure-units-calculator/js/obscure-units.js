function find_combinations_greedy(target_number, df) {
  let solution = [];
  let combination_sum = 0;
  let best_solution = [0, 9999];

  for (let row of shuffle(df)) {
    if (row.maximum_base + combination_sum <= target_number) {
      solution.push(row.name);
      combination_sum += row.maximum_base;

      if (Math.abs(combination_sum - target_number) < best_solution[1]) {
        best_solution = [
          solution.slice(),
          Math.abs(combination_sum - target_number),
        ];
      }

      if (Math.abs(combination_sum - target_number) < Number.EPSILON) {
        return [solution, combination_sum];
      }

      for (let j = 2; j <= Math.floor(target_number / row.maximum_base); j++) {
        if (row.maximum_base * j + combination_sum <= target_number) {
          for (let k = 0; k < j; k++) {
            solution.push(row.name);
          }

          combination_sum += row.maximum_base * j;

          if (Math.abs(combination_sum - target_number) < best_solution[1]) {
            best_solution = [
              solution.slice(),
              Math.abs(combination_sum - target_number),
            ];
          }

          if (Math.abs(combination_sum - target_number) < Number.EPSILON) {
            return [solution, combination_sum];
          } else {
            while (solution.length > 0 && combination_sum > target_number) {
              let last = solution.pop();
              combination_sum -= row.maximum_base;

              if (
                Math.abs(combination_sum - target_number) < best_solution[1]
              ) {
                best_solution = [
                  solution.slice(),
                  Math.abs(combination_sum - target_number),
                ];
              }
            }

            if (Math.abs(combination_sum - target_number) < Number.EPSILON) {
              return [solution, combination_sum];
            }
          }

          if (solution.length == 0) {
            break;
          }
        }
      }
    }
  }

  const counts = {};
  best_solution[0].forEach(function (x) {
    counts[x] = (counts[x] || 0) + 1;
  });

  let sortable = [];
  for (var name in counts) {
    sortable.push([name, counts[name]]);
  }

  return sortable.sort(function (a, b) {
    return b[1] - a[1];
  });
}

function shuffle(array) {
  let currentIndex = array.length,
    temporaryValue,
    randomIndex;

  while (0 !== currentIndex) {
    randomIndex = Math.floor(Math.random() * currentIndex);
    currentIndex -= 1;

    temporaryValue = array[currentIndex];
    array[currentIndex] = array[randomIndex];
    array[randomIndex] = temporaryValue;
  }

  return array;
}

